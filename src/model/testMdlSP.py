import numpy as np
import scipy.signal as signal
import pickle
import copy
from scipy.signal import dlti, dlsim
import time
from sys import getsizeof


#######################################################################################################################
# Function
#######################################################################################################################
def testMdlSP(data, t, setupPar, setupMdl, setupExp):
    ###################################################################################################################
    # MSG IN
    ###################################################################################################################
    print("INFO: Testing Model (SP)")

    ###################################################################################################################
    # Initialisation
    ###################################################################################################################
    # ==============================================================================
    # Parameters
    # ==============================================================================
    mdlName = 'mdl/mdl_' + setupPar['model'] + '_' + setupExp['name'] + '.pckl'

    # ==============================================================================
    # Variables
    # ==============================================================================
    dataPred = {'T': {}}
    dataPred['T']['X'] = copy.deepcopy(data['T']['X'])
    t = t - t[0]

    ###################################################################################################################
    # Pre-Processing
    ###################################################################################################################
    # ==============================================================================
    # Reshape data
    # ==============================================================================
    if np.size(data['T']['X'].shape) == 3:
        data['T']['X'] = data['T']['X'].reshape((data['T']['X'].shape[0], data['T']['X'].shape[1] * data['T']['X'].shape[2]))

    # ==============================================================================
    # Load model
    # ==============================================================================
    file = open(mdlName, 'rb')
    mdl = pickle.load(file)

    ###################################################################################################################
    # Calculation
    ###################################################################################################################
    # ==============================================================================
    # Start timer
    # ==============================================================================
    start = time.time()

    # ==============================================================================
    # State-Space Model
    # ==============================================================================
    if setupPar['model'] == 'SS':
        sys = dlti(mdl.a, mdl.b, mdl.c, mdl.d, dt=t[1]-t[0])
        _, dataPred['T']['y'], _  = sys.output(data['T']['X'], time[0:data['T']['X'].shape[0]], x0=np.zeros(setupMdl['SP_SS_order']))

    # ==============================================================================
    # State-Space Model
    # ==============================================================================
    if setupPar['model'] == 'TF':
        sys = signal.TransferFunction(mdl.num, mdl.den, dt=t[1]-t[0])
        _, dataPred['T']['y'] = dlsim(sys, u=data['T']['X'][:, 0], t=time)

    # ==============================================================================
    # End timer
    # ==============================================================================
    ende = time.time()
    testTime = (ende - start)

    ###################################################################################################################
    # Post-Processing
    ###################################################################################################################
    print("INFO: Total inference time (ms): %.2f" % (testTime * 1000))
    print("INFO: Inference time per sample (us): %.2f" % (testTime / data['T']['X'].shape[0] * 1000 * 1000))
    print("INFO: Model size (kB): %.2f" % (getsizeof(mdl) / 1024 / 8))

    ###################################################################################################################
    # References
    ###################################################################################################################
    return dataPred

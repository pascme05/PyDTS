from scipy import signal


#######################################################################################################################
# Function
#######################################################################################################################
def filterData(data, method, window):
    ###################################################################################################################
    # MSG IN
    ###################################################################################################################
    print("INFO: Filtering Data")

    ###################################################################################################################
    # Calculation
    ###################################################################################################################
    # ==============================================================================
    # Median Filter
    # ==============================================================================
    if method == 1:
        for name in data:
            data[name] = signal.medfilt(data[name], kernel_size=window)

    ###################################################################################################################
    # Return
    ###################################################################################################################
    return data
